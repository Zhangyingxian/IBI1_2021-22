def alignment(m,n):
	edit_distance=0
	score=0
	for i in range(len(m)):
		if m[i]!=n[i]:
			edit_distance +=1
		x=amino_acid.index(m[i])
		y=amino_acid.index(n[i])
		score=score+BLOSUM62[x][y]
	z=1-edit_distance/len(m)
	p=z*100
	print('The percentage of identical amino acids is '+str(p)+'%')
	print('The alignment score is '+str(score))
